#include<stdio.h>
#include<math.h>

int main() {

    int num1 = 6;
    int num2 = 4;

    printf("%f\n",5.0 + 4.5);
    printf("%f\n",5.0 - 4.5);
    printf("%f\n",5.0 * 4.5);
    printf("%f\n",5.0 / 4.5);

    printf("%d\n",num1 + num2);
    printf("%d\n",num1 - num2);
    printf("%d\n",num1 * num2);
    printf("%d\n",num1 / num2);

    // Power
    printf("%.f\n",pow(2,5));
    printf("%f\n", ceil(36.356));
    printf("%f\n", floor(36.956));

    return 0;
}